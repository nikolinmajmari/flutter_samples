class AuthCard extends StatefulWidget {
  const AuthCard({
    Key key,
  }) : super(key: key);

  @override
  _AuthCardState createState() => _AuthCardState();
}


 void _showErrorDialog(String message,context){
  showDialog(context: context,builder: (ctx)=>
  AlertDialog(
    title: Text("Error App"),
    content: Text(message),
    actions: <Widget>[FlatButton(child: Text("ok"),onPressed: ()=>Navigator.of(ctx).pop(),)],
  ));
 }

class _AuthCardState extends State<AuthCard> with SingleTickerProviderStateMixin{
  final GlobalKey<FormState> _formKey = GlobalKey();
  AuthMode _authMode = AuthMode.Login;
  Map<String, String> _authData = {
    'email': 'a@gmail.com',
    'password': 'xapixapi',
  };
  var _isLoading = false;
  final _passwordController = TextEditingController();
  AnimationController _controller;
  Animation<Size> _heightAnimation;
  Animation<double> _opacityAnimation;
  @override
  void initState() {
    // TODO: implement setState
    super.initState();
    _controller = AnimationController(vsync: this,duration: Duration(milliseconds: 500));
    _heightAnimation = Tween<Size>(begin:Size(double.infinity,260),end: Size(double.infinity,320)).animate(
      CurvedAnimation(
        parent: _controller,
        curve: Curves.easeOutQuad
      )
    );
    _opacityAnimation = Tween<double>(begin: 0,end: 1).
    animate(
      CurvedAnimation(
        parent: _controller,
        curve: Curves.easeInOutSine
      )
    );
    _heightAnimation.addListener(() {setState((){});});
  }
  @override
  void dispose() {
    // TODO: implement dispose
    _controller.dispose();
    super.dispose();
  }

  void _submit(BuildContext context)async {
    _passwordController.value = (TextEditingValue(text: "xapixapi"));
    if (!_formKey.currentState.validate()) {
      // Invalid!
      return;
    }
    _formKey.currentState.save();
    setState(() {
      _isLoading = true;
    });
    if (_authMode == AuthMode.Login) {
       try {
         await  Provider.of<Auth>(context,listen: false).login(_authData["email"],_authData["password"]);
       }
      on HttpException catch (error){
        var errorMsg = error.toString();
        _showErrorDialog(errorMsg, context);
      }catch(error){
        var errorMsg = "could not authenticate";
        _showErrorDialog(errorMsg, context);
      }
    } else {
      print("singing up");
    try {
      await Provider.of<Auth>(context, listen: false).singup(
          _authData["email"], _authData["password"]);
    }
    on HttpException catch (error){
      var errorMsg = "AuthenticationFailed";
      if(error.toString().contains("EMAIL_EXISTS"))
        errorMsg = "Email is already in use";
      else if(error.toString().contains("INVALID_EMAIL"))
        errorMsg ="Invalid email address";
      else errorMsg = error.toString();
      _showErrorDialog(errorMsg, context);
    }catch(error){
      var errorMsg = "could not authenticate";
    }
    }
    setState(() {
      _isLoading = false;
    });
  }

  void _switchAuthMode() {
    if (_authMode == AuthMode.Login) {
      setState(() {
        _authMode = AuthMode.Signup;
      });
      _controller.forward();
    } else {
      setState(() {
        _authMode = AuthMode.Login;
      });
      _controller.reverse();
    }
  }

